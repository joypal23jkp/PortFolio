class Social {
  get name() {
    return this._name;
  }
  set name(value) {
    this._name = value;
  }

  get link() {
    return this._link;
  }
  set link(value) {
    this._link = value;
  }

  get img() {
    return this._img;
  }
  set img(value) {
    this._img = value;
  }
}

export default Social;
