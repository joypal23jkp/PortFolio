class Experience {
  get designation() {
    return this._designation;
  }
  set designation(value) {
    this._designation = value;
  }
  get company() {
    return this._company;
  }
  set company(value) {
    this._company = value;
  }

  get endDate() {
    return this._endDate;
  }
  set endDate(value) {
    this._endDate = value;
  }

  get startingDate() {
    return this._startingDate;
  }
  set startingDate(value) {
    this._startingDate = value;
  }

  get details() {
    return this._details;
  }
  set details(value) {
    this._details = value;
  }
}

export default Experience;
