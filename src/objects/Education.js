class Education {
  get course() {
    return this._course;
  }
  set course(value) {
    this._course = value;
  }

  get major() {
    return this._major;
  }
  set major(value) {
    this._major = value;
  }

  get university() {
    return this._university;
  }
  set university(value) {
    this._university = value;
  }

  get passingYear() {
    return this._passingYear;
  }
  set passingYear(value) {
    this._passingYear = value;
  }
}

export default Education;
